#ifndef FONCTIONS_TRIGO_C
#define FONCTIONS_TRIGO_C

#include "fonctions_trigo.h"

// calcul de cos(x)
double mon_cos(double x)
{
	return cos(x);
}

// calcul de sin(x)
double mon_sin(double x)
{
	return sin(x);
}

// calcul de tan(x)
double mon_tan(double x)
{
	return tan(x);
}

#endif
